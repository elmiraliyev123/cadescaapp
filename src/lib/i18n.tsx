type NestedKeyOf<ObjectType extends object> = {
  [Key in keyof ObjectType & (string | number)]: ObjectType[Key] extends object
    ? `${Key}.${NestedKeyOf<ObjectType[Key]>}`
    : `${Key}`;
}[keyof ObjectType & (string | number)];

type TranslationKey = NestedKeyOf<typeof translations.en>;

interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: TranslationKey) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

function writeLocaleCookie(value: Language) {
  if (typeof document === "undefined") return;
  const domain = getLocaleCookieDomain();
  document.cookie = [
    `${NEXT_LOCALE_COOKIE_NAME}=${encodeURIComponent(value)}`,
    domain ? `Domain=${domain}` : "",
    "Path=/",
    `Max-Age=${LOCALE_COOKIE_MAX_AGE}`,
    "SameSite=Lax",
    window.location.protocol === "https:" ? "Secure" : ""
  ].filter(Boolean).join("; ");
}

export function LanguageProvider({
  children,
  initialLanguage = "en"
}: {
  children: React.ReactNode;
  initialLanguage?: Language;
}) {
  const [language, setLanguageState] = useState<Language>(initialLanguage);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    writeLocaleCookie(lang);

    fetch("/api/user/locale", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ locale: lang })
    }).catch(() => {
      // Silently ignore — cookie is already set client-side
    });
  };

  const t = (key: TranslationKey): string => {
    const keys = key.split(".");
    let value: any = translations[language];
    for (const k of keys) {
      if (value[k] === undefined) {
        // Fallback to english
        let fallbackValue: any = translations.en;
        for (const fk of keys) {
          if (fallbackValue[fk] === undefined) return key;
          fallbackValue = fallbackValue[fk];
        }
        return fallbackValue;
      }
      value = value[k];
    }
    return value;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      <div suppressHydrationWarning>{children}</div>
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
